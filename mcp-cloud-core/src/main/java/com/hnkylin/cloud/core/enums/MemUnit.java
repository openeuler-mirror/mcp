package com.hnkylin.cloud.core.enums;

public enum MemUnit {
    MB(0, "MB"),
    GB(1, "GB"),
    ;


    MemUnit(int value, String desc) {
        this.value = value;
        this.desc = desc;
    }

    private int value;
    private String desc;

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
